# string operation
name = "shruti sunil pote"
# 1.len()function - for calculating late of given string
print(len(name)) 
# 2.upper()method - use for capitlize the string
print(name.upper())
# 3.lower()method - use for smallcase
print(name.lower())
# 4.capitlize()method - use for 1st letter to capital
print(name.capitalize())
# 5.Title()method - use for each letter of each string for the capital
print(name.title())
# 6.count()method -used for count the given letter is how many times in string
print(name.count("s"))
# 7.replace - for replacing
print(name.replace(" ","*"))
print(name.find("shruti"))